package com.example.rupizza;

/**
 * Enum representing the different types of pizza crusts.
 * Each crust type is associated with a specific pizza style:
 *     Chicago: Deep Dish, Pan, Stuffed
 *     New York: Brooklyn, Thin, Hand-Tossed
 * This enum is used in the Pizza class to specify the crust type for a pizza.
 *
 * @author Divit Shetty (dps190)
 */
public enum Crust {
    DEEP_DISH,    // Chicago
    PAN,          // Chicago
    STUFFED,      // Chicago
    BROOKLYN,     // New York
    THIN,         // New York
    HAND_TOSSED   // New York
}
